def register_fonts():
    candidates = [
        Path(os.environ.get("WINDIR", "C:/Windows")) / "Fonts" / "arial.ttf",
        Path(os.environ.get("WINDIR", "C:/Windows")) / "Fonts" / "Arial.ttf",
        Path("/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf"),
    ]
    for p in candidates:
        if p.exists():
            pdfmetrics.registerFont(TTFont("CyrillicFont", str(p)))
            return "CyrillicFont"
    return "Helvetica"


FONT = register_fonts()


def write_pdf(path: Path, title: str, paragraphs: list[str]):
    styles = getSampleStyleSheet()
    style = styles["Normal"]
    style.fontName = FONT
    style.fontSize = 11
    style.leading = 14
    title_style = styles["Heading1"]
    title_style.fontName = FONT
    title_style.fontSize = 14

    doc = SimpleDocTemplate(
        str(path),
        pagesize=A4,
        leftMargin=20 * mm,
        rightMargin=20 * mm,
        topMargin=20 * mm,
        bottomMargin=20 * mm,
    )
    story = [Paragraph(title, title_style), Spacer(1, 8)]
    for p in paragraphs:
        story.append(Paragraph(p.replace("\n", "<br/>"), style))
        story.append(Spacer(1, 6))
    doc.build(story)


def write_docx(path: Path, title: str, paragraphs: list[str]):
    doc = Document()
    doc.add_heading(title, level=1)
    for p in paragraphs:
        para = doc.add_paragraph(p)
        para.style.font.size = Pt(11)
    doc.core_properties.title = title
    doc.core_properties.author = CLINIC
    doc.save(path)


def write_txt(path: Path, title: str, paragraphs: list[str]):
    lines = [title, "=" * len(title), "", CLINIC, f"Дата актуализации: {datetime.now():%d.%m.%Y}", ""]
    lines.extend(paragraphs)
    lines.extend(["", f"Контакты: {PHONE}, {SITE}, {EMAIL}"])
    path.write_text("\n".join(lines), encoding="utf-8")


def write_md(path: Path, title: str, paragraphs: list[str]):
    lines = [f"# {title}", "", f"**{CLINIC}**  ", f"*Актуально на {datetime.now():%d.%m.%Y}*", ""]
    n = 0
    for p in paragraphs:
        if not p:
            lines.append("")
            continue
        if p.startswith("#"):
            lines.append(p)
        elif p.startswith("-"):
            lines.append(p)
        else:
            n += 1
            lines.append(f"{n}. {p}")
        lines.append("")
    lines.append(f"---\n\nКонтакты: {PHONE} | [Сайт]({SITE}) | {EMAIL}")
    path.write_text("\n".join(lines), encoding="utf-8")


